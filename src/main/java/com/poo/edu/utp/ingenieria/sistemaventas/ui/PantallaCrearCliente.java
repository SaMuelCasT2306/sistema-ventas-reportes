    private void agregarCliente() throws SQLException {

        try {

            if (jTextField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", "Validación", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (jTextField2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", "Validación", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (jTextField3.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", "Validación", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String rucStr = jTextField2.getText();
            if (rucStr.length() < 11) {
                JOptionPane.showMessageDialog(this, "El RUC debe de tener 11 dígitos", "Validación", JOptionPane.WARNING_MESSAGE);
                return;
            }
            String nombre = jTextField1.getText();
            String direccion = jTextField3.getText();
            Long ruc = Long.parseLong(rucStr);

            Cliente cliente = new Cliente();
            cliente.setRuc(ruc);
            cliente.setNombre(nombre);
            cliente.setDireccion(direccion);
            control.guardarCliente(cliente);

            JOptionPane.showMessageDialog(this, "Cliente agregado con exito", "Validación", JOptionPane.WARNING_MESSAGE);
            
            limpiarFormulario();

        } catch (Exception e) {
            System.out.println("Error :" + e.getMessage() + e.getLocalizedMessage() + e.toString());
        }

    }

    private void limpiarFormulario() {

        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");

    }

